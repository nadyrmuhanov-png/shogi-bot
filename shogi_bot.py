# ==================== НОВАЯ ГЕНЕРАЦИЯ КАРТОЧКИ ФИГУРЫ ====================
def draw_piece_card(piece):
    """Генерирует стильную карточку фигуры с традиционной формой комы и кандзи"""
    W, H = 450, 480
    # Красивый мягкий фон карточки
    img = Image.new("RGB", (W, H), color=(245, 242, 235))
    draw = ImageDraw.Draw(img)

    # Список шрифтов для фолбека (важно для поддержки иероглифов на Linux/Windows/macOS)
    font_paths = [
        "msmincho.ttc", "msgothic.ttc", "Arial Uni.ttf", 
        "TakaoMincho.ttf", "NotoSansCJK-Regular.ttc", "arial.ttf"
    ]
    
    font_big, font_mid, font_name, font_small = None, None, None, None
    for f_path in font_paths:
        try:
            font_big = ImageFont.truetype(f_path, 110)
            font_mid = ImageFont.truetype(f_path, 50)
            font_name = ImageFont.truetype(f_path, 32)
            font_small = ImageFont.truetype(f_path, 20)
            break
        except Exception:
            continue

    if not font_big:
        font_big = font_mid = font_name = font_small = ImageFont.load_default()

    # --- РИСУЕМ ТРАДИЦИОННУЮ ФИГУРУ СЁГИ (ПЯТИУГОЛЬНИК) ---
    # Координаты вершин пятиугольника (слегка сужается кверху)
    # Вершина (топ), верхний правый, нижний правый, нижний левый, верхний левый
    poly_points = [
        (W // 2, 40),      # Верхний пик
        (W - 80, 90),      # Верхний правый угол
        (W - 60, 290),     # Нижний правый угол
        (60, 290),         # Нижний левый угол
        (80, 90)           # Верхний левый угол
    ]
    
    # Тень фигуры
    shadow_points = [(p[0] + 4, p[1] + 5) for p in poly_points]
    draw.polygon(shadow_points, fill=(210, 205, 195))
    
    # Сама деревянная плашка (традиционный цвет древесины хоноки/самшит)
    draw.polygon(poly_points, fill=(242, 198, 124), outline=(139, 90, 43), width=4)

    # --- ОТРИСОВКА ИЕРОГЛИФОВ ---
    cx = W // 2
    
    if piece["promoted_kanji"]:
        # Если есть сторона превращения, рисуем основной (крупно слева) и превращенный (красным справа)
        draw.text((cx - 45, 175), piece["kanji"], font=font_big, fill=(30, 30, 30), anchor="mm")
        # Символ превращения традиционно пишется красным (темно-красный для читаемости)
        draw.text((cx + 55, 185), piece["promoted_kanji"], font=font_mid, fill=(180, 30, 30), anchor="mm")
        
        # Маленькая подпись-индикатор сторон
        draw.text((cx - 45, 85), "Основная", font=font_small, fill=(120, 90, 50), anchor="mm")
        draw.text((cx + 55, 115), "Перевернутая", font=font_small, fill=(150, 70, 70), anchor="mm")
    else:
        # Если фигура не превращается (Король, Золото), иероглиф строго по центру
        draw.text((cx, 170), piece["kanji"], font=font_big, fill=(30, 30, 30), anchor="mm")

    # --- ТЕКСТОВЫЙ БЛОК СНИЗУ ---
    # Название на ромадзи / японском
    draw.text((cx, 345), piece["name"], font=font_name, fill=(60, 30, 0), anchor="mm")
    
    # Русское название в красивой рамке
    ru_txt = f" {piece['ru_name']} "
    draw.rounded_rectangle([cx - 130, 380, cx + 130, 415], radius=5, fill=(225, 215, 195))
    draw.text((cx, 396), ru_txt, font=font_small, fill=(100, 40, 0), anchor="mm")

    # Декоративный футер
    draw.text((cx, 450), "将棋 • Japanese Chess", font=font_small, fill=(160, 140, 120), anchor="mm")

    buf = io.BytesIO()
    img.save(buf, format="PNG")
    buf.seek(0)
    return buf
